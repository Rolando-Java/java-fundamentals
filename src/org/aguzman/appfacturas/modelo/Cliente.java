package org.aguzman.appfacturas.modelo;

public class Cliente {

    private String nombre;
    private String nif;

    public String getNombre() {
        return this.nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getNif() {
        return this.nif;
    }

    public void setNif(String nif) {
        this.nif = nif;
    }
}
